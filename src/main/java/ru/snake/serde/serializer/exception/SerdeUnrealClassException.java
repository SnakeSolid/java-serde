package ru.snake.serde.serializer.exception;

public class SerdeUnrealClassException extends SerdeException {

	private static final long serialVersionUID = 449651620544281661L;

	public SerdeUnrealClassException() {
		super();
	}

	public SerdeUnrealClassException(String message) {
		super(message);
	}

	public SerdeUnrealClassException(String message, Throwable cause) {
		super(message, cause);
	}

	public SerdeUnrealClassException(Throwable cause) {
		super(cause);
	}

}
