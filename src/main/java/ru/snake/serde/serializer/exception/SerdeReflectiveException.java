package ru.snake.serde.serializer.exception;

public class SerdeReflectiveException extends SerdeException {

	private static final long serialVersionUID = -7419244001359652194L;

	public SerdeReflectiveException() {
		super();
	}

	public SerdeReflectiveException(String message) {
		super(message);
	}

	public SerdeReflectiveException(String message, Throwable cause) {
		super(message, cause);
	}

	public SerdeReflectiveException(Throwable cause) {
		super(cause);
	}

}
